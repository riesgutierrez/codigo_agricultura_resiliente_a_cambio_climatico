  var ndvi = img.normalizedDifference(['B8','B4']).rename('NDVI')
  img = img.addBands(ndvi)
  
// Se calcula el indice NDWI de la coleccion de imagenes y se adiciona
// como una nueva banda en la feature collection
  
  var ndwi = img.normalizedDifference(['B8','B11']).rename('NDWI')
  img = img.addBands(ndwi)
  
// Se calcula el indice EVI de la coleccion de imagenes y se adiciona
// como una nueva banda en la feature collection
  
  var evi = img.expression('2.5*(nir-red)/(nir+(red*C)-(B*blue)+L)',
  {
    nir : img.select('B8'),
    red : img.select('B4'),
    blue : img.select('B2'),
    C : 7.5,
    B : 6,
    L : 1
  }).rename('EVI')
  img = img.addBands(evi)
  
// Se calcula el indice SAVI de la coleccion de imagenes y se adiciona
// como una nueva banda en la feature collection
  
  var savi = img.expression('((nir-red)/(nir+red+L))*(1+L)',
  {
    nir : img.select('B8'),
    red : img.select('B4'),
    L : 1
  }).rename('SAVI')
  img = img.addBands(savi)
  
// Se calcula el indice ARVI de la coleccion de imagenes y se adiciona
// como una nueva banda en la feature collection
  
   var arvi = img.expression('(nir-(2*red)+blue)/(nir+(2*red)+blue)',
  {
    nir : img.select('B8'),
    red : img.select('B4'),
    blue : img.select('B2'),
  }).rename('ARVI')
  img = img.addBands(arvi)
  
// Se establece el calculo de los diferentes indices para todas 
// las imagenes de la coleccion para el poligono de la geometria establecida

  return img.select(['NDVI', 'NDWI', 'EVI', 'SAVI', 'ARVI']).clip(geometry)
